def enc_msg(key, msg: bytes):
    # print(msg)
    if type(msg) == str:
        msg = msg.encode('utf-8')
    if type(key) == str:
        key = int(key)

    char_based_key = key % 256

    ret = []
    for char in msg:
        ret.append(char ^ char_based_key)
        char_based_key = (char_based_key + 13) % 256
    return bytes(ret)


